# Usando blocos de condição
# if / elif  / else
# se / se não se / senao
entrada = input('Voce quer "entrar" ou "Sair": ')

if entrada == 'entrar':
    print('Voce entrou no sistema')
    print('login feito bestão')
    print('....')
elif entrada == 'sair':
    print('Voce saiu do sistema')

    print(50)
else:
    print('Informação desconhecida')


print('Sai dos blocos condicionais')

